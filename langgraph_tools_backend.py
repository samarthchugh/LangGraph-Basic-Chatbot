from langgraph.graph import StateGraph, START, END
from typing import TypedDict, Annotated
from langchain_core.messages import BaseMessage, HumanMessage
from langchain_groq import ChatGroq
from langgraph.checkpoint.sqlite import SqliteSaver
from langgraph.graph.message import add_messages
from langchain_core.tools import tool
from langchain_community.tools import DuckDuckGoSearchResults
from langgraph.prebuilt import ToolNode, tools_condition
import requests
from dotenv import load_dotenv
import sqlite3
import os
load_dotenv()
GROQ_API_KEY = os.getenv("GROQ_API_KEY")

# 1. LLM
llm = ChatGroq(api_key=GROQ_API_KEY, model="llama-3.1-8b-instant")

# 2. tools
search_tool = DuckDuckGoSearchResults(region='us-en')

@tool
def calculator(first_num: float, second_num: float, operation: str) -> dict:
    """
    Perform a basic arithmetic operation on two numbers.
    Supported operations: add, sub, mul, div
    """
    try:
        if operation == "add":
            result = first_num + second_num
        elif operation == "sub":
            result = first_num - second_num
        elif operation == "mul":
            result = first_num * second_num
        elif operation == "div":
            if second_num == 0:
                return {"error": "Division by zero is not allowed"}
            result = first_num / second_num
        else:
            return {"error": f"Unsupported operation '{operation}'"}
        
        return {"first_num": first_num, "second_num": second_num, "operation": operation, "result": result}
    except Exception as e:
        return {"error": str(e)}
    
@tool
def get_stock_price(symbol: str) -> dict:
    """
    Fetch latest stock price for a given symbol (e.g. 'AAPL', 'TSLA') 
    using Alpha Vantage with API key in the URL.
    """
    url = f"https://www.alphavantage.co/query?function=GLOBAL_QUOTE&symbol={symbol}&apikey=ZPYH63OE2W7H372P"
    r = requests.get(url)
    return r.json()

tools = [search_tool, get_stock_price, calculator]
llm_with_tools = llm.bind_tools(tools)

# 3. State

class ChatState(TypedDict):
    messages: Annotated[list[BaseMessage], add_messages]
    
# 4. Nodes
def chat_node(state: ChatState) -> ChatState:
    messages = state["messages"]
    response = llm_with_tools.invoke(messages)
    return {'messages': [response]}

tool_node = ToolNode(tools)

# 5. Checkpointer

conn = sqlite3.connect('chatbot_database.db', check_same_thread=False)

checkpoint = SqliteSaver(conn=conn)

# 6. graph

graph = StateGraph(ChatState)
graph.add_node("chat_node", chat_node)
graph.add_node("tools", tool_node)

graph.add_edge(START, "chat_node")
graph.add_conditional_edges("chat_node", tools_condition)
graph.add_edge("tools", "chat_node")
# graph.add_edge("chat_node", END)

chatbot = graph.compile(checkpointer=checkpoint)

# 7. Helper functions
def retrieve_all_threads():
    all_threads = set()
    for i in checkpoint.list(None):
        all_threads.add(i.config['configurable']['thread_id'])

    return list(all_threads)